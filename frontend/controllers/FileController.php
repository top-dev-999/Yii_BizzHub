            ]
        ];
    }

    /**
     * @return string
     */
    public function actionDownload($n,$p,$a) {    
        $file = \Yii::getAlias('@storage').'/web/'.$p.'/'.$n;
        if (file_exists($file)) {
           Yii::$app->response->sendFile($file);
        }
    }
